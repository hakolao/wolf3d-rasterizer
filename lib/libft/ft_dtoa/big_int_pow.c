void			mul_lhs_by_small_exponent(t_big_int *lhs, uint32_t exponent,
				t_big_int *curr)
{
	if ((exponent & 0x7) != 0)
		big_int_mul_u32(lhs, g_power_of_10_u32[(exponent & 0x7)], curr);
	else
		*curr = *lhs;
}

/*
** Outputs a big int of 10^exponent into result
*/

void			big_int_pow_10(uint32_t exponent, t_big_int *res)
{
	t_big_int	temps[2];
	t_big_int	*curr;
	t_big_int	*next;
	t_big_int	*swap;
	uint32_t	table_index;

	curr = &temps[0];
	next = &temps[1];
	big_int_set_u32(curr, g_power_of_10_u32[exponent & 0x7]);
	exponent >>= 3;
	table_index = 0;
	while (exponent != 0)
	{
		if (exponent & 1)
		{
			big_int_mul(curr, &g_power_of_10_big[table_index], next);
			swap = curr;
			curr = next;
			next = swap;
		}
		table_index++;
		exponent >>= 1;
	}
	*res = *curr;
}

/*
** Multiplies a big int with 10^exponent into result
*/

void			big_int_mul_pow_10(t_big_int *lhs, uint32_t exponent,
				t_big_int *res)
{
	t_big_int	temps[2];
	t_big_int	*curr;
	t_big_int	*next;
	t_big_int	*swap;
	uint32_t	table_index;

	curr = &temps[0];
	next = &temps[1];
	mul_lhs_by_small_exponent(lhs, exponent, curr);
	exponent >>= 3;
	table_index = 0;
	while (exponent != 0)
	{
		if (exponent & 1)
		{
			big_int_mul(curr, &g_power_of_10_big[table_index], next);
			swap = curr;
			curr = next;
			next = swap;
		}
		table_index++;
		exponent >>= 1;
	}
	*res = *curr;
}

/*
** Outputs a big int of 2^exponent into result
*/

void			big_int_pow_2(uint32_t exponent, t_big_int *res)
{
	uint32_t	block_i;
	uint32_t	bit_i;
	size_t		i;

	block_i = exponent / 32;
	i = -1;
	while (++i <= block_i)
		res->blocks[i] = 0;
	res->length = block_i + 1;
	bit_i = (exponent % 32);
	res->blocks[block_i] |= (1 << bit_i);
}
