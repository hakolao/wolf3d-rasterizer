uint32_t				log_base2_32(uint32_t val)
{
	uint32_t				temp;

	temp = val >> 24;
	if (temp)
		return (24 + log_table(temp));
	temp = val >> 16;
	if (temp)
		return (16 + log_table(temp));
	temp = val >> 8;
	if (temp)
		return (8 + log_table(temp));
	return (log_table(val));
}

uint32_t				log_base2_64(uint64_t val)
{
	uint64_t	tmp;

	tmp = val >> 32;
	if (tmp)
		return (32 + log_base2_32((uint32_t)tmp));
	return (log_base2_32((uint32_t)val));
}
